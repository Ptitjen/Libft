#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*t;

	t = *lst;
	if (*lst)
	{
		while (t->next)
			t = t->next;
		t->next = new;
	}
	else
		*lst = new;
}
